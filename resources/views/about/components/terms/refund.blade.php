    <h3 class="text-primary font-weight-semi-bold">Cancelamento e Reembolso de Transações</h3>

    <p>
        O site permite que o ORGANIZADOR solicite ao Tikket, até o 2º dia útil após o término do evento, o cancelamento de uma venda e o consequente reembolso do valor pago pelo
        Consumidor. É de responsabilidade do ORGANIZADOR comunicar a seus potenciais Consumidores e Participantes sua própria política de cancelamento, que deverá ser compatível
        com o período máximo de reembolso permitido pelo Tikket e solicitar, quando considerar necessário, o cancelamento da transação através das ferramentas presentes na
        plataforma.
    </p>

    <p>
        É de responsabilidade do USUÁRIO informar-se sobre a política específica do evento que deseja participar e, caso necessário, entrar em contato com o ORGANIZADOR para
        solicitar o cancelamento de sua compra e o consequente reembolso do pagamento.
    </p>

    <p>
        O Participante não poderá solicitar reembolso de seus ingressos, o que poderá ser feito tão somente pelo USUÁRIO (pessoa que realizou a compra na plataforma Tikket), salvo
        quando Participante e USUÁRIO forem a mesma pessoa.
    </p>

    <p>
        Caso seja necessário reembolsar Consumidores, o ORGANIZADOR deverá, necessariamente, fazê-lo através da plataforma Tikket, sendo vedada a realização do reembolso por meios
        diversos.
    </p>

    <p>
        Após o início do procedimento de reembolso pelo ORGANIZADOR, nas compras realizadas por cartão de crédito, o USUÁRIO receberá um comunicado confirmando o cancelamento da
        compra e o reembolso será realizado de maneira imediata e automática. Nas compras feitas por débito ou boleto, o USUÁRIO receberá um e-mail contendo uma solicitação de
        preenchimento de dados bancários para que o reembolso seja realizado.
    </p>

    <p>
        O USUÁRIO será responsável por qualquer equívoco ou atraso no cadastramento dos dados bancários para reembolso dos valores.
    </p>

    <p>
        Se identificarmos que algum evento não foi realizado ou, foi realizado de maneira distinta ao anunciado, o Tikket se resguarda o direito de reembolsar todas as compras ou
        mesmo suspender, provisoriamente, o valor do repasse até que a situação esteja regularizada entre todas as partes envolvidas na transação, quais sejam, Tikket, ORGANIZADOR
        e Consumidores.
    </p>

    <p>
        Especificamente em casos de compras com indícios de fraude, o setor de Análise de Risco do Tikket poderá solicitar ao USUÁRIO a cópia digitalizada dos documentos do titular
        do cartão de crédito utilizado nas compras, bem como do próprio cartão utilizado, visando legitimá-las. Neste caso, o contato será feito através dos nossos canais oficiais.
    </p>

    <p>
        O prazo de envio de tal documentação será estabelecido pelo Tikket, de acordo com a data e horário em que o evento ocorrerá, de forma que, remetido o documento pelo
        USUÁRIO, o Tikket fará a análise do mesmo e dará um retorno através do mesmo e-mail utilizado para a compra, ou outro canal de contato oficial.
    </p>

    <p>
        Caso os documentos não sejam enviados pelo USUÁRIO no prazo determinado, ficará a cargo do Tikket cancelar/suspender, ou não, a transação, a seu exclusivo critério. O
        Tikket, ainda, poderá solicitar ao USUÁRIO que seja apresentado, obrigatoriamente, na portaria do evento, um documento de identidade, com foto do titular do cartão ou o
        próprio cartão utilizado na compra, para fins de validação da compra. Em todo o caso, se o USUÁRIO não enviar as informações requeridas o Tikket poderá cancelar a transação
        sem que tal fato gere expectativa de direito ou indenização de qualquer espécie.
    </p>

    <p>
        Em caso de arrependimento da compra, o reembolso do valor pago estará garantido, caso a solicitação seja feita ao ORGANIZADOR, no prazo de até 7 (sete) dias, a contar da
        data compra. O pedido de devolução deverá ser realizado, preferencialmente, no prazo de 48 (quarenta e oito) horas antes do horário de início do evento.
    </p>
</div>
