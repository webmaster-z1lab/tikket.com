    <h3 class="text-primary font-weight-semi-bold">Venda de Ingressos</h3>

    <p>
        O processamento dos pagamentos das vendas de ingressos para eventos inicia-se, geralmente, na página do evento publicado pelo ORGANIZADOR. O USUÁRIO escolherá o tipo de ingresso desejado e o método de pagamento, considerando as opções disponíveis, e em seguida, finalizará o pedido.
    </p>
    <p>
        Após a confirmação de aprovação do pagamento, que poderá ser feita por intermediário financeiro, o Tikket recolherá o valor correspondente, deduzirá a taxa de serviço e repassará ao ORGANIZADOR o valor restante. Todas as transações realizadas através d Tikket passam por processos internos, automatizados, de análise de risco que poderão ser complementados pelos nossos processadores de pagamentos.
    </p>
    <p>
        Por segurança, as compras realizadas utilizando cartão de crédito que necessitem de confirmação ou validação adicional poderão passar pelo processo de análise aprofundada. Neste procedimento, o Tikket poderá requerer o envio de documentos pessoais para verificação da identidade do usuário.
    </p>
    <p>
        Caso o Tikket identifique alguma inconsistência no procedimento de pagamento, a compra poderá ser submetida a um novo processo de verificação, a qualquer momento. O USUÁRIO será notificado por e-mail, no mesmo endereço associado a compra, caso o pedido seja suspenso ou cancelado pelo Tikket.
    </p>
</div>
