    <h3 class="text-primary font-weight-semi-bold">Taxa de Serviço e emissão de Nota Fiscal</h3>

    <p>
        Será cobrada do Organizador uma taxa de serviço para o uso da plataforma Tikket, calculada a partir das vendas online processadas através dela. A taxa cobrada é de 10% (dez
        por cento) sobre o valor do ingresso, com um valor mínimo de taxa de R$ 5,00 (cinco reais) por ingresso.
    </p>

    <p>
        O Tikket somente emite Nota Fiscal para ORGANIZADORES, tendo em vista serem estes os tomadores dos serviços prestados.
    </p>
</div>
