    <h3 class="text-primary font-weight-semi-bold">Contestações e Extornos</h3>

    <p>
        As contestações reportadas serão analisadas pela operadora de cartão de crédito, por nossos processadores de pagamentos, ou pelo próprio Tikket.
    </p>

    <p>
        O Tikket será responsável por quaisquer disputas abertas pelos USUÁRIOS, tais como, mas não somente, chargebacks (contestações), reclamações diversas, cancelamento e estornos. Assim, irá iniciar e processar cancelamentos sem a necessidade da intervenção ou aprovação do ORGANIZADOR, nos casos de:
    </p>

    <ol>
        <li>
            não cumprimento da política de reembolsos do evento e/ou do Código de Defesa do Consumidor, por parte do ORGANIZADOR;
        </li>
        <li>
            detecção de indícios de fraude em compras realizadas, estejam elas pendentes de aprovação ou já aprovadas, sejam tais riscos provenientes do USUÁRIO ou do ORGANIZADOR;
        </li>
        <li>
            cancelamento do evento;
        </li>
        <li>
            erro técnico no processamento da transação;
        </li>
        <li>
            verificarmos um número elevado de queixas referentes a um determinado evento, por parte dos USUÁRIOS.
        </li>
    </ol>
