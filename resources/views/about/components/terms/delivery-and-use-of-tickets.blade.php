    <h3 class="text-primary font-weight-semi-bold">Entrega e Utilização dos Ingressos</h3>

    <p>
        Os ingressos comprados através do Tikket serão enviados ao e-mail indicado pelo USUÁRIO assim que o pagamento for aprovado. Pagamentos realizados por boleto podem levar até
        72 horas para serem aprovados.
    </p>

    <p>
        Cabe ao USUÁRIO deixar seus sistemas de anti-spam, filtros ou similares, de modo que não interfiram no recebimento dos comunicados e materiais do Tikket, não sendo
        aceitável nenhuma escusa caso não tenha tido acesso a algum e-mail ou mensagem eletrônica em virtude dos recursos mencionados, o que não gerará qualquer tipo de expectativa
        de direito ou indenização ao USUÁRIO.
    </p>

    <p>
        Além da entrega por meio de mensagem eletrônica, os ingressos também poderão ser acessados na área autenticada "MEUS INGRESSOS" no site. Para isso, o USUÁRIO deve acessar o
        site Tikket, fornecendo o mesmo e-mail e senha utilizado no seu registro.
    </p>

    <p>
        Os ingressos são associados a um código alfanumérico único, representado em algarismos, “QR Code” e Código de Barras. Após o recebimento, caberá ao USUÁRIO mantê-los em
        segurança, não divulgando-os publicamente, sobretudo em redes sociais, ou permitindo o acesso de terceiros a ele.
    </p>

    <p>
        Os ingressos comprados através do Tikket são nominais, cabendo ao ORGANIZADOR decidir pela conferência dessa informação no momento de acesso ao evento. A não conferência
        dessa informação não gerará qualquer tipo de expectativa de direito ou indenização.
    </p>

    <p>
        Os ingressos adquiridos no Tikket podem ser usados para acesso ao evento uma única vez. O check-in do PARTICIPANTE, ou seja, a leitura e validação do ingresso do USUÁRIO,
        através do identificador único, é de responsabilidade do ORGANIZADOR. O acesso do Participante ao evento não será liberado caso o check-in do ingresso já tenha sido
        realizado anteriormente, salvo por decisão exclusiva do ORGANIZADOR.
    </p>
</div>
