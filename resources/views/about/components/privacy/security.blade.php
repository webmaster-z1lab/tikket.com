<p>Tomamos todas as providências para protegê-lo de interceptação, mau uso e adulteração de suas informações pessoais, dentre outras práticas indesejáveis ou mesmo ilegais que
    ocorrem na internet. Quando informações a seu respeito são transmitidas, isto é feito num ambiente seguro, no qual os dados são criptografados (criptografia é o nome dado ao
    processo de codificação de informações). As informações são codificadas (embaralhadas) na origem e decodificadas no destino, dificultando, dessa forma, que as informações que
    trafegam na internet sejam decifradas. O ícone “Cadeado Fechado”, localizado na barra de status (parte inferior direita do monitor), indica que as informações serão
    criptografadas. Você também poderá identificar a segurança com as URLs das páginas seguras a partir da nomenclatura “https” antes do domínio, por exemplo:
    https://tikket.com.br.</p>

<p>As informações identificáveis e que são coletadas por nós são manipuladas apenas por nosso pessoal autorizado.</p>

<p>Os dados pessoais armazenados em nossos servidores são protegidos por procedimentos e sistemas de segurança.</p>

<p>O SITE se empenha para prover segurança e sigilo das informações que capta.</p>

<p>Contudo, é sabido que nenhum dado está 100% seguro na internet. Sendo assim, embora o SITE sempre envide seus melhores esforços para proteger suas informações pessoais, não é
    possível garantir a segurança de todas as informações que você venha a nos fornecer, o que torna a utilização do serviço nessas condições uma decisão pessoal do USUÁRIO.</p>
